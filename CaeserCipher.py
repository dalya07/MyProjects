# Creat the Function
def ceaser(start_text, shift_amount, cipher_direction):
    # انشاء سلسلة اي string
    end_text = ""
    if cipher_direction == "decode":
        # 5 * -1 = -5
        shift_amount *= -1
    # Each of the char in the start_text
    for char in start_text:
        if char in alphabet:
            # Um den Index des Buchstabens herauszufinden, also so zusagen die stelle
            position = alphabet.index(char)
            # Die position des Buchstabens + das shift_amount
            new_position = position + shift_amount
            # end_text = end_text + alphabet[new_position]
            end_text += alphabet[new_position]
        else:
            # end_text = end_text + char
            end_text += char
    print(f"The {cipher_direction}d text is: {end_text}")

